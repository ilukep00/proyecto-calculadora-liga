                    jTextField7.setText(jornadas[6][jornada-1]);
                    jTextField8.setText(jornadas[7][jornada-1]);
                    jTextField9.setText(jornadas[8][jornada-1]);
                    jTextField10.setText(jornadas[9][jornada-1]);
                    jTextField11.setText(jornadas[10][jornada-1]);
                    jTextField12.setText(jornadas[11][jornada-1]);
                    jTextField13.setText(jornadas[12][jornada-1]);
                    jTextField14.setText(jornadas[13][jornada-1]);
